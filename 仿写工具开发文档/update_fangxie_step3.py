# -*- coding: utf-8 -*-
"""
第三步：修改提示词部分，强化禁止故事的规则
"""

print("开始第三步修改...")

# 读取文件
with open('fangxie_tool.py', 'r', encoding='utf-8') as f:
    content = f.read()

# 1. 在build_prompt函数开头添加最高优先级警告
old_prompt_start = '''        prompt = f"""请根据以下参考文案进行仿写，生成{article_count}篇全新的百家号引流文案。

【第一优先级任务】
每篇文案先在内部写完正文，再提炼5个不同标题，最后按结构输出"标题区 + 正文"。
如果你漏掉任意一篇的5个标题，这次输出就视为失败。'''

new_prompt_start = '''        prompt = f"""请根据以下参考文案进行仿写，生成{article_count}篇全新的百家号引流文案。

【最高优先级警告】
1. **绝对禁止故事开头**：不得出现任何"我有个朋友/邻居/便利店/地铁/昨天"等故事场景
2. **必须用玄学大气风格**：使用"大千世界/天地/宇宙/道德经云/孩子/徒儿"等开头
3. **只说"你"，不讲别人**：全文只能描述读者自己的处境，不能讲任何第三方的故事

【第一优先级任务】
每篇文案先在内部写完正文，再提炼5个不同标题，最后按结构输出"标题区 + 正文"。
如果你漏掉任意一篇的5个标题，这次输出就视为失败。'''

content = content.replace(old_prompt_start, new_prompt_start)
print("已添加最高优先级警告")

# 2. 修改opening_hook_protocol
old_opening_hook = '''        opening_hook_protocol = """## 开头钩子硬规则
- 每篇开头前2句必须出现钩子（痛点钩子/反差钩子/结果钩子/身份钩子四选一）。
- 开头可以打招呼，也可以直接情绪切入，但必须快速抓住读者注意力。
- 禁止故事叙述式开场：禁止"那天/有一次/我认识/有人问我/我朋友"这类叙事触发句。
"""'''

new_opening_hook = '''        opening_hook_protocol = """## 开头钩子硬规则（最高优先级！）
- **【绝对禁止】任何故事叙述式开场**：
  - 禁止："那天/有一次/我认识/有人问我/我朋友/我有个/昨天/前几天"
  - 禁止："便利店/地铁/超市/菜市场/出租车/邻居/大妈/老板娘/司机"
  - 禁止："我妈说/我爸说/我爷爷/同事问/朋友说"等任何故事场景
  - 禁止任何具体人物、具体场景、具体时间的叙述
- **【必须使用】玄学大气风格开头**：
  - 使用宏观意象：大千世界/芸芸众生/天地/宇宙/乾坤/红尘/星辰
  - 使用经典引用：道德经云/易经讲/古人云/老祖宗说
  - 使用直接点醒：你到底有多值钱/你活成了最特别的谜底
  - 使用身份认同：孩子/徒儿/老友/道友/有缘人
- 每篇开头前2句必须出现钩子（痛点钩子/反差钩子/结果钩子/身份钩子四选一）。
- 开头必须快速抓住读者注意力，直击人心，不绕弯。
"""'''

content = content.replace(old_opening_hook, new_opening_hook)
print("已修改opening_hook_protocol")

# 保存文件
with open('fangxie_tool.py', 'w', encoding='utf-8') as f:
    f.write(content)

print("第三步修改完成！")
