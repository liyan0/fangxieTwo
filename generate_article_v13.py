content_3 = """我问你一个问题，你认真想想再回答：

你是不是经常觉得，自己明明很努力，可日子就是不见好？

你起得比别人早，睡得比别人晚，干得比别人多，可升职加薪没你的份，好事轮不到你，坏事一件不落。你开始怀疑：是不是老天爷跟我过不去？是不是我这辈子就这样了？

我告诉你真相：不是老天爷跟你过不去，是你自己跟自己过不去。

你的好日子，正在被你自己挡住。

怎么挡的？三个字：不敢要。

你不敢要属于自己的功劳，明明是你干的，你说"大家一起努力的"；你不敢要应得的回报，明明该涨工资了，你说"再等等吧"；你不敢要别人的帮助，明明扛不动了，你说"我自己能行"；你不敢要自己想要的东西，明明心里痒痒，你说"算了，不合适"。

你把"不好意思"刻进了骨子里，把"我不配"当成了口头禅。别人夸你两句，你赶紧摆手说"没有没有"；别人给你机会，你第一反应是"我行吗"；别人对你好，你心里想的是"我拿什么还"。

鲁迅说过："中国人的性情是总喜欢调和、折中的，譬如你说，这屋子太暗，须在这里开一个窗，大家一定不允许的。但如果你主张拆掉屋顶，他们就会来调和，愿意开窗了。"你就是那个永远只敢提"开窗"的人，从来不敢提"拆屋顶"。你把自己的需求压到最低，低到别人都忘了你也是需要被照顾的。

你知道吗？你越是不敢要，别人越是觉得你不需要。你越是退让，别人越是觉得理所当然。你以为的懂事，在别人眼里就是"好欺负"；你以为的大度，在别人眼里就是"无所谓"。你以为自己在做好人，其实你在做老好人。好人有底线，老好人没有。

莫言说过一句话："世界上的事情，最忌讳的就是个十全十美，你看那天上的月亮，一旦圆满了，马上就要亏厌。"你一直想做那个十全十美的人，照顾所有人的感受，满足所有人的需求，结果把自己累得半死，还落不着好。你委屈了自己，成全了别人，到头来别人还觉得你应该的。

你该学会"自私"一点了。

这个"自私"不是让你去伤害别人，是让你学会把自己的需求放在前面。该争取的争取，该拒绝的拒绝，该开口的开口。你不替自己说话，没人会替你说话；你不为自己争取，没人会为你争取。你的好日子，不会从天上掉下来，得你自己去拿。

可问题是，道理你都懂，就是做不到。为什么？因为你心里有个结，这个结不解开，你永远迈不出那一步。你被"不好意思"绑住了，被"我不配"困住了，被从小到大的教育框住了。

这个结是什么，怎么解，说起来话长，这里也不是细聊的地方。

但如果你真的想搞明白，想知道怎么把挡在你面前的那堵墙推倒，现在就点开我的头像，去看我主页置顶的那两条内容。你挡了自己这么久，也该给自己让让路了。

去吧。你的好日子，就在你放过自己的那一刻开始。"""

