# 正文生成协议（3月7日改造 + 3月14日扩充）
explosive_content_protocol = """## 爆款正文硬规则（8大核心规则）

【规则1：开头钩子（前100字内）】
5种爆款开头公式（必须二选一）：
1. 紧急警告型："完蛋了，屏幕前的你！" / "注意了！"
2. 喜讯通知型："恭喜你！" / "好消息来了！"
3. 秘密揭露型："有个人背着你..." / "有件事瞒不住了..."
4. 身份定位型："能刷到这条的，都不是普通人"
5. 玄学权威型："宇宙已经..." / "大佬说了..."

【规则2：前段（200-400字）：揭示危机/秘密】
- 引入"他们"（那些看不起你的人）
- 揭示"他们"的行为（背后说你、算计你）
- 制造对立（你 VS 他们）
- 禁止平铺直叙，必须有冲突感

【规则3：中段（400-800字）：反转+认可（情绪高潮）】
- 反转：其实你不是软弱，你是在憋大招
- 认可：权威/宇宙/大佬看好你
- 情绪爆发：他们急了、怕了、后悔了
- 每150-200字必须有一个情绪爆发点

【规则4：后段（800-1200字）：引经据典+升华】
- 引用古语（自然，不生硬）
- 继续制造情绪
- 禁止引用现代作家、禁止讲心理学

【规则5：结尾（最后100字）：明确引流+玄学加持】
- 明确引导：点开我的头像，去主页橱窗里看看
- 制造紧迫：不用想太多，跟着感觉走
- 玄学加持：你的好运，从这一刻开始
- 禁止只有祝福，必须有引流动作

【规则6：第三方角色使用规则（最重要！）】
- 每段都必须有第三方视角
- 平均每150-200字必须出现一次第三方角色
- 制造"你 VS 他们"的对立
- 制造"权威认可你"的爽感
- 降低"他们"使用频率到60%（不是每篇都要有）
- 降低"你是不是"开头使用率到10%以下

【规则7：语言风格硬规则】
- 口语化：完蛋了、炸锅了、急死了、傻眼了
- 短句化：平均句长15-20字
- 重复强调：重要的话重复3遍
- 禁止书面化：禁止引用现代作家、禁止讲心理学术语
- 禁止英文：任何情况下都不得出现英文单词

【规则8：情绪节奏控制】
- 情绪曲线：开头爆发 → 前段紧张 → 中段高潮 → 后段持续 → 结尾拉升
- 情绪爆发点密度：平均每150-200字一个
- 禁止温吞水、禁止平缓叙述
"""

